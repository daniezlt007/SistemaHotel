    private void jButton_SalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_SalvarActionPerformed
        // TODO add your handling code here:
        if (txtNomeTrabalhador.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Favor preencher o campo Nome do Funcionário!");
            txtNomeTrabalhador.requestFocus();
            return;
        }

        if (txtCpfTrabalhador.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Favor preencher o campo CPF do Funcionário!");
            txtCpfTrabalhador.requestFocus();
            return;
        }

        if (txtRgTrabalhador.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Favor preencher o campo RG do Funcionário!");
            txtRgTrabalhador.requestFocus();
            return;
        }
        
        if (txtSalario.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Favor preencher o campo Salário do Funcionário!");
            txtSalario.requestFocus();
            return;
        }
        
        if (txtLogin.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Favor preencher o campo Login do Funcionário!");
            txtLogin.requestFocus();
            return;
        }

        if (txtSenha.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Favor preencher o campo Senha do Funcionário!");
            txtSenha.requestFocus();
            return;
        }

        DadTrabalhador dts = new DadTrabalhador();
        LogTrabalhador func = new LogTrabalhador();
        
        int selecionado;
        
        dts.setNome(txtNomeTrabalhador.getText());
        dts.setCpf(txtCpfTrabalhador.getText());
        dts.setRg(txtRgTrabalhador.getText());
        dts.setEndereco(txtEnderecoTrabalhador.getText());
        dts.setEmail(txtEmailTrabalhador.getText());
        dts.setFone_res_com(txtFoneResTrabalhador.getText());
        dts.setFone_cel(txtCelularTrabalhador.getText());
        dts.setSalario(Double.parseDouble(txtSalario.getText()));
        
        selecionado = jComboBox_Acesso.getSelectedIndex();
        dts.setAcesso((String)jComboBox_Acesso.getItemAt(selecionado));
        
        dts.setLogin(txtLogin.getText());
        dts.setSenha(txtSenha.getText());
        
        selecionado = jComboBoxEstado.getSelectedIndex();
        dts.setEstado((String) jComboBoxEstado.getItemAt(selecionado));

        if (acao.equals("salvar")) {
            if (func.inserir(dts)) {
                JOptionPane.showMessageDialog(rootPane, "O Trabalhador foi salvo com sucesso!");
                mostrar("");
                inabilitar();
            }
        } else if (acao.equals("editar")) {
            dts.setIdpessoa(Integer.parseInt(txtIdPessoa.getText()));

            if (func.editar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "O Trabalhador foi editado com sucesso!");
                mostrar("");
                inabilitar();
            }
        }
    }//GEN-LAST:event_jButton_SalvarActionPerformed

