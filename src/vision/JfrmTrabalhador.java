    private void TabelaListaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TabelaListaMouseClicked
        // TODO add your handling code here:
        jButton_Salvar.setText("Editar");
        habilitar();
        jButton_Excluir.setEnabled(true);
        acao = "editar";

        int fila = TabelaLista.rowAtPoint(evt.getPoint());

        txtIdPessoa.setText(TabelaLista.getValueAt(fila, 0).toString());
        txtNomeTrabalhador.setText(TabelaLista.getValueAt(fila, 1).toString());
        txtCpfTrabalhador.setText(TabelaLista.getValueAt(fila, 2).toString());
        txtRgTrabalhador.setText(TabelaLista.getValueAt(fila, 3).toString());
        txtEnderecoTrabalhador.setText(TabelaLista.getValueAt(fila, 4).toString());
        txtFoneResTrabalhador.setText(TabelaLista.getValueAt(fila, 5).toString());
        txtCelularTrabalhador.setText(TabelaLista.getValueAt(fila, 6).toString());
        txtEmailTrabalhador.setText(TabelaLista.getValueAt(fila, 7).toString());
        
        txtSalario.setText(TabelaLista.getValueAt(fila, 8).toString());
        jComboBox_Acesso.setSelectedItem(TabelaLista.getValueAt(fila, 9).toString());
        txtLogin.setText(TabelaLista.getValueAt(fila,10).toString());
        txtSenha.setText(TabelaLista.getValueAt(fila,11).toString());
        jComboBoxEstado.setSelectedItem(TabelaLista.getValueAt(fila, 12));
    }//GEN-LAST:event_TabelaListaMouseClicked

