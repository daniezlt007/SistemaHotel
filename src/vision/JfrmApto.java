    private void jButton_SalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_SalvarActionPerformed
        // TODO add your handling code here:
        if (txtApto.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Favor preencher um Número de Apto");
            txtApto.requestFocus();
            return;
        }

        if (txtDescricao.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Deves preencher a Descrição!");
            txtDescricao.requestFocus();
            return;
        }

        if (txtPrecoDiaria.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Deves preencher o Valor da Diária!");
            txtPrecoDiaria.requestFocus();
            return;
        }

        if (txtCaracteristicas.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Deves preencher as Características!");
            txtCaracteristicas.requestFocus();
            return;
        }

        DadHabitacao dts = new DadHabitacao();
        LogHabitacao func = new LogHabitacao();

        dts.setApto(txtApto.getText());

        int selecionado = JcomboAndar.getSelectedIndex();
        dts.setAndar((String) JcomboAndar.getItemAt(selecionado));

        dts.setDescricao(txtDescricao.getText());
        dts.setCaracteristica(txtCaracteristicas.getText());

        dts.setPreco_diaria(Double.parseDouble(txtPrecoDiaria.getText()));

        selecionado = JcomboStatus.getSelectedIndex();
        dts.setStatus((String) JcomboStatus.getItemAt(selecionado));

        selecionado = JcomboTipoApto.getSelectedIndex();
        dts.setTipo_habitacao((String) JcomboTipoApto.getItemAt(selecionado));

        if (acao.equals("salvar")) {
            if (func.inserir(dts)) {
                JOptionPane.showMessageDialog(rootPane, "O apto foi salvo com sucesso!");
                mostrar("");
                inabilitar();
            }
        } else if (acao.equals("editar")) {
            dts.setIdhabitacao(Integer.parseInt(txtIdhabitacao.getText()));

            if (func.editar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "O apto foi editado com sucesso!");
                mostrar("");
                inabilitar();
            }
        }
    }//GEN-LAST:event_jButton_SalvarActionPerformed

    private void jButton_ExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_ExcluirActionPerformed
        // TODO add your handling code here:
        if (!txtIdhabitacao.getText().equals("")) {
            int confirmacao = JOptionPane.showConfirmDialog(rootPane, "Tem certeza que deseja excluir o apto?", "Confirmar", 2);

            if (confirmacao == 0) {
                LogHabitacao func = new LogHabitacao();
                DadHabitacao dts = new DadHabitacao();

                dts.setIdhabitacao(Integer.parseInt(txtIdhabitacao.getText()));
                func.excluir(dts);
                mostrar("");
                inabilitar();
            }
        }
    }//GEN-LAST:event_jButton_ExcluirActionPerformed

