    private void TabelaListaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TabelaListaMouseClicked
        // TODO add your handling code here:
        jButton_Salvar.setText("Editar");
        habilitar();
        jButton_Excluir.setEnabled(true);
        acao = "editar";

        int fila = TabelaLista.rowAtPoint(evt.getPoint());

        txtIdhabitacao.setText(TabelaLista.getValueAt(fila, 0).toString());
        txtApto.setText(TabelaLista.getValueAt(fila, 1).toString());
        
        JcomboAndar.setSelectedItem(TabelaLista.getValueAt(fila, 2).toString());
        txtDescricao.setText(TabelaLista.getValueAt(fila, 3).toString());
        txtCaracteristicas.setText(TabelaLista.getValueAt(fila, 4).toString());
        txtPrecoDiaria.setText(TabelaLista.getValueAt(fila, 5).toString());
        
        JcomboStatus.setSelectedItem(TabelaLista.getValueAt(fila, 6).toString());
        JcomboTipoApto.setSelectedItem(TabelaLista.getValueAt(fila, 7).toString());

    }//GEN-LAST:event_TabelaListaMouseClicked

